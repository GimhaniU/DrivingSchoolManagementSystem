    int sameRow = 0;
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        Date date = dateChooserM.getDate();

        String dateS = sdf.format(date);
        dtm = (DefaultTableModel) attendanceTable.getModel();
        if (dtm.getRowCount() > 0) {
            for (int i = 0; i < dtm.getRowCount(); i++) {
                if (dateS.equals(dtm.getValueAt(i, 0).toString())
                        & turnCombo.getSelectedItem().toString().equals(dtm.getValueAt(i, 1).toString())
                        & getStudent().equals(dtm.getValueAt(i, 2).toString())
                        & instructorCombo.getSelectedItem().toString().equals(dtm.getValueAt(i, 3).toString())) {
                    sameRow++;
                }
            }
            if (sameRow == 0) {

                dtm.addRow(new Object[]{dateS, turnCombo.getSelectedItem().toString(), getStudent(), instructorCombo.getSelectedItem().toString()});
                alreadyLabel.setVisible(false);
            } else {
                alreadyLabel.setVisible(true);
            }
            sameRow = 0;
        } else if (dtm.getRowCount() == 0) {
            dtm.addRow(new Object[]{dateS, turnCombo.getSelectedItem().toString(), getStudent(), instructorCombo.getSelectedItem().toString()});

            alreadyLabel.setVisible(false);
        }
    }//GEN-LAST:event_addButtonActionPerformed
    private String getStudent() {
        String studentId = null;
        if (studentOwn.isSelected()) {
            studentId = ownStudentCombo.getSelectedItem().toString();
        } else if (studentOther.isSelected()) {
            studentId = otherStudentCombo.getSelectedItem().toString();
        }
        return studentId;
    }
