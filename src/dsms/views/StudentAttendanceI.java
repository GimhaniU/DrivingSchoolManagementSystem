    private void addAttendanceToTable() {
        String date = sdf.format(dateChooserV.getDate());
        try {
            dtmV = (DefaultTableModel) attendanceEditTable.getModel();
            ArrayList<StudentAttendance> attendances = StudentAttendanceController.searchAttendanceOfDate(date);
            for (StudentAttendance attendance : attendances) {
                Instructor instructor = InstructorController.searchInstructor(attendance.getInstructorId());
                Registration registration = RegistrationController.searchRegistration(attendance.getRegistrationId());
                Student student = StudentController.searchStudent(registration.getStudentId());
                dtm = (DefaultTableModel) attendanceTable.getModel();
                dtmV.addRow(new Object[]{attendance.getTurn(), student.getStudentId(), student.getNameWithInitials(), attendance.getInstructorId(), instructor.getName()});
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(StudentAttendanceI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

