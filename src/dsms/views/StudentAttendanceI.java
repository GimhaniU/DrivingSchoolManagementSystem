    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        int j = 0, availableCount = 0;
        for (int i = 0; i < dtm.getRowCount(); i++) {
            try {
                Instructor instructor = InstructorController.searchInstructor(dtm.getValueAt(i, 3).toString());
                Registration registration = RegistrationController.searchRegistration(dtm.getValueAt(i, 2).toString(), instructor.getClassId());
                StudentAttendance studentAttendance = new StudentAttendance(registration.getRegistrationId(), dtm.getValueAt(i, 3).toString(), dtm.getValueAt(i, 0).toString(), dtm.getValueAt(i, 1).toString());
                boolean isPresent = StudentAttendanceController.searchAttendance(studentAttendance);
                if (isPresent == true) {
                    availableCount++;

                } else {
                    j += StudentAttendanceController.addStudentAttendance(studentAttendance);
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(StudentAttendanceI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (j == (dtm.getRowCount() - availableCount)) {
            JOptionPane.showMessageDialog(StudentAttendanceI.this, "Attendance saved!");
        }
        dtm.setRowCount(0);
    }//GEN-LAST:event_saveButtonActionPerformed

