    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            Instructor instructor = new Instructor();
            instructor.setInstructorId(iidTextR.getText());
            instructor.setName(nameWithInitialsTextR.getText());
            instructor.setAddress(addressTextR.getText());
            instructor.setInstructorTel(telephoneTextR.getText());
            instructor.setNic(nicTextR.getText());
            Date date = dateChooser.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            instructor.setDateOfRegistration(sdf.format(date));
            instructor.setDriverLicenceNo(driLicNoTextR.getText());
            if (dlExpireDateChooser.getDate() != null) {
                Date dateExp = dlExpireDateChooser.getDate();
                instructor.setDriverLicenceExpireDate(sdf.format(dateExp));
            }
            instructor.setTeachingLicenceNo(teachDriLicNoTextR.getText());
            if (tdlExpireDateChooser.getDate() != null) {
                Date dateTDl = tdlExpireDateChooser.getDate();
                instructor.setTeachingLicenceExpireDate(sdf.format(dateTDl));
            }
            VehicleClassSet vehicleClassSet = VehicleClassController.searchVehicleClassSetByName(classComboBox1.getSelectedItem().toString());
            instructor.setClassId(vehicleClassSet.getClassId());
            //  Instructor instructor = getDataFromForm(iidTextR.getText());
            int res = InstructorController.addNewInstructor(instructor);
            if (res > 0) {
                JOptionPane.showMessageDialog(InstructorDetailManager.this, "Registration successful!");
            } else {
                JOptionPane.showMessageDialog(InstructorDetailManager.this, "Failed!");
            }
            ComboItemsAdder.addAllInstructors(instructorComboBox);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(InstructorDetailManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

