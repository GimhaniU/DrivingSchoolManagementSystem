    private void saveAButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAButtonActionPerformed
        dtm = (DefaultTableModel) tableAttendance.getModel();
        ArrayList<InstructorAttendance> attendanceSet = new ArrayList<>();
        Date dateM = dateChooserM.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(dateM);
        try {
            for (int i = 0; i < dtm.getRowCount(); i++) {
                Instructor instructor = InstructorController.searchInstructorByName(dtm.getValueAt(i, 0).toString());
                if (instructor != null) {
                    InstructorAttendance instructorAttendance = new InstructorAttendance(instructor.getInstructorId(), date, dtm.getValueAt(i, 2).toString(), dtm.getValueAt(i, 3).toString());
                    attendanceSet.add(instructorAttendance);
                }

            }
            int res = InstructorAttendanceController.addInstructorAttendance(attendanceSet);
            if (res > 0) {
                JOptionPane.showMessageDialog(InstructorDetailManager.this, "Saved!");
            } else {
                JOptionPane.showMessageDialog(InstructorDetailManager.this, "Failed!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(InstructorDetailManager.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_saveAButtonActionPerformed

