    private void backUpFileChooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backUpFileChooserActionPerformed
     if(JFileChooser.APPROVE_OPTION==1){  
        File f=backUpFileChooser.getSelectedFile();
       String path=f.getAbsolutePath();
        try {
           int backUpDone= dbUtilities.BackUp.writeBackup(path);
           if(backUpDone>0){
               JOptionPane.showMessageDialog(this,"Back up created at "+path+"");               
           }else{
              JOptionPane.showMessageDialog(this,"Unable to create the back up!"); 
           }
           this.dispose();
        } catch (IOException | InterruptedException ex) {
            Logger.getLogger(BackUp.class.getName()).log(Level.SEVERE, null, ex);
        }
     }else if(JFileChooser.CANCEL_OPTION==1){
         this.dispose();
     }
       
        
    }//GEN-LAST:event_backUpFileChooserActionPerformed

